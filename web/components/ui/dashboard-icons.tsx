export type DashboardStatKind =
  | "trophy"
  | "star"
  | "flame"
  | "card"
  | "goal"
  | "conceded";

export function DashboardStatIcon({
  name,
}: {
  name: DashboardStatKind;
}) {
  switch (name) {
    case "trophy":
      return <IconTrophy />;
    case "star":
      return <IconStar />;
    case "flame":
      return <IconFlame />;
    case "card":
      return <IconCardWarning />;
    case "goal":
      return <IconGoal />;
    case "conceded":
      return <IconConceded />;
    default:
      return <IconTrophy />;
  }
}
